  // CREATING AND RENDERING THE GRID
  const squares = [];
  function createBoard() {
    for (let i = 0; i < layout.length; i++) {
      const square = document.createElement("div");
      // putting squares in the grid
      grid.appendChild(square);
      squares.push(square);

      // adding layout to the board
      if (layout[i] === 0) {
        squares[i].classList.add("pac-dot");
      } else if (layout[i] === 1) {
        squares[i].classList.add("wall");
      } else if (layout[i] === 2) {
        squares[i].classList.add("ghost-lair");
      } else if (layout[i] === 3) {
        squares[i].classList.add("power-pellet");
      }
    }
  }
  createBoard();

  // CREATING PAC-MAN AT START POSITION
  let pacmanCurrentIndex = 490;
  squares[pacmanCurrentIndex].classList.add("pac-man");

  // CREATING MOVE COMMANDS
  function movePacman(e) {
    // first remove the starting position
    squares[pacmanCurrentIndex].classList.remove("pac-man");

    switch (e.keyCode) {
      // checking direction, wall and ghost lair
      // moving left
      case 37:
        if (
          pacmanCurrentIndex % width !== 0 &&
          !squares[pacmanCurrentIndex - 1].classList.contains("wall") &&
          !squares[pacmanCurrentIndex - 1].classList.contains("ghost-lair")
        )
          pacmanCurrentIndex -= 1;

        // check if pacman is in the left exit
        if (pacmanCurrentIndex - 1 === 363) {
          pacmanCurrentIndex = 391;
        }
        break;
      //moving up
      case 38:
        if (
          pacmanCurrentIndex - width >= 0 &&
          !squares[pacmanCurrentIndex - width].classList.contains("wall") &&
          !squares[pacmanCurrentIndex - width].classList.contains("ghost-lair")
        )
          pacmanCurrentIndex -= width;
        break;
      //moving right
      case 39:
        if (
          pacmanCurrentIndex % width < width - 1 &&
          !squares[pacmanCurrentIndex + 1].classList.contains("wall") &&
          !squares[pacmanCurrentIndex + 1].classList.contains("ghost-lair")
        )
          pacmanCurrentIndex += 1;
        // check if pacman is in the right exit
        if (pacmanCurrentIndex - 1 === 391) {
          pacmanCurrentIndex = 363;
        }
        break;
      // moving down
      case 40:
        if (
          pacmanCurrentIndex - width >= 0 &&
          !squares[pacmanCurrentIndex + width].classList.contains("wall") &&
          !squares[pacmanCurrentIndex + width].classList.contains("ghost-lair")
        )
          pacmanCurrentIndex += width;
        break;
    }

    squares[pacmanCurrentIndex].classList.add("pac-man");

    //pacDotEaten

    //powerPelletEaten

    //checkForGameOver

    //checkForWin()
  }

  document.addEventListener("keyup", movePacman);
